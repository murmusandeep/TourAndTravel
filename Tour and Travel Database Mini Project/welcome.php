    <h1 class="text-center">Tour and Travel</h1>  

    <p>Tourism is travel for pleasure or business; also the theory and practice of touring, the business of attracting, accommodating, and entertaining tourists, and the business of operating tours.[2] Tourism may be international or within the traveler's country. The World Tourism Organization defines tourism more generally, in terms which go "beyond the common perception of tourism as being limited to holiday activity only", as people "traveling to and staying in places outside their usual environment for not more than one consecutive year for leisure and not less than 24 hours, business and other purposes".[3]
        Tourism can be domestic or international, and international tourism has both incoming and outgoing implications on a country's balance of payments.
        Tourism suffered as a result of a strong economic slowdown of the late-2000s recession, between the second half of 2008 and the end of 2009, and the outbreak of the H1N1 influenza virus,[4][5] but slowly recovered. International tourism receipts (the travel item in the balance of payments) grew to US$1.03 trillion (€740 billion) in 2005, corresponding to an increase in real terms of 3.8% from 2010.[6] International tourist arrivals surpassed the milestone of 1 billion tourists globally for the first time in 2012,[7] emerging markets such as China, Russia, and Brazil had significantly increased their spending over the previous decade.[8] The ITB Berlin is the world's leading tourism trade fair.[9] Global tourism accounts for ca. 8% of global greenhouse gas emissions.[10]
    </p>
    <h1>Etymology</h1><br>
    <p>
        The word tourist was used in 1772[11] and tourism in 1811.[12] It is formed from the word tour, which is derived from Old English turian, from Old French torner, from Latin tornare; 'to turn on a lathe,' which is itself from Ancient Greek tornos (τόρνος); 'lathe'.[13]
    </p>
    <h1>Significance of tourism</h1><br>
    <p>
        The tourism industry, as part of the service sector,[14] has become an important source of income for many regions and even for entire countries. The Manila Declaration on World Tourism of 1980 recognized its importance as "an activity essential to the life of nations because of its direct effects on the social, cultural, educational, and economic sectors of national societies, and on their international relations."[3][15]
        Tourism brings large amounts of income into a local economy in the form of payment for goods and services needed by tourists, accounting as of 2011 for 30% of the world's trade in services, and, as an invisible export, for 6% of overall exports of goods and services.[6] It also generates opportunities for employment in the service sector of the economy associated with tourism.[16]
        The hospitality industries which benefit from tourism include transportation services (such as airlines, cruise ships, trains and taxicabs); lodging (including hotels, hostels, homestays, resorts and renting rooms); and entertainment venues (such as amusement parks, restaurants, casinos, shopping malls, music venues, and theatres). This is in addition to goods bought by tourists, including souvenirs.
        On the flip-side, tourism can degrade people[17] and sour relationships between host and guest.[18]
    </p>
